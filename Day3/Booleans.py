# Mayor que
numero = 5 > 2 + 3
print(type(numero))
print(numero)

# Menor que
numero = 5 < 2 + 3
print(type(numero))
print(numero)

# Igual
numero = 5 == 2 + 3
print(numero)

# Mayor igual
numero = 5 >= 2 + 3
print(type(numero))
print(numero)

# Diferente
numero = 5 != 2 + 3
print(type(numero))
print(numero)

